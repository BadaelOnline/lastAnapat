                <div class="cma-wysiwyg-editor"><h5 style="text-align: justify;"><strong>1. ¿QUÉ ES UNA COOKIE?</strong>
                    </h5>
                    <p style="text-align: justify;">Una cookie es un archivo o dispositivo que se descarga en el
                        ordenador, tableta o móvil cuando el usuario navega en Internet, con la finalidad de almacenar
                        datos que podrán ser actualizados y recuperados por la entidad responsable de su
                        instalación.</p>
                    <p style="text-align: justify;">La primera finalidad de las Cookies es la de facilitar al Usuario un
                        acceso más rápido a los Servicios seleccionados. Además, las Cookies personalizan los Servicios
                        que ofrece la Web, facilitando y ofreciendo a cada Usuario información que es de su interés o
                        que puede ser de su interés, en atención al uso que realiza de los Servicios.</p>
                    <p style="text-align: justify;">La Web utiliza Cookies para personalizar y facilitar al máximo la
                        navegación del Usuario. Las Cookies se asocian únicamente a un Usuario anónimo y su ordenador y
                        no proporcionan referencias que permitan deducir datos personales del Usuario. El Usuario podrá
                        configurar su navegador para que notifique y rechace la instalación de las Cookies enviadas por
                        la Web, sin que ello perjudique la posibilidad del Usuario de acceder a los Contenidos. Sin
                        embargo, debe tener en cuenta, en todo caso, que la calidad de funcionamiento de la página Web
                        puede disminuir.</p>
                    <p style="text-align: justify;">Los Usuarios registrados que se registren o que hayan iniciado
                        sesión, podrán beneficiarse de unos servicios más personalizados y orientados a su perfil,
                        gracias a la combinación de los datos almacenados en las cookies con los datos personales
                        utilizados en el momento de su registro. Dichos Usuarios autorizan expresamente el uso de esta
                        información con la finalidad indicada, sin perjuicio de su derecho a rechazar o deshabilitar el
                        uso de cookies.</p><h5 style="text-align: justify;"><strong>2. NUESTRA POLÍTICA SOBRE
                            COOKIES</strong></h5>
                    <p style="text-align: justify;">Para utilizar este sitio web al completo el usuario deberá aceptar
                        las cookies ya que muchas funcionalidades online han sido diseñadas específicamente para
                        funcionar por medio del uso de cookies. Es importante señalar que las cookies que se han
                        instalado por medio de este sitio web no guardan información de carácter personal suya o datos
                        sensibles.</p>
                    <p style="text-align: justify;">A continuación, se realiza una clasificación de las cookies en
                        función de una serie de categorías. No obstante es necesario tener en cuenta que una misma
                        cookie puede estar incluida en más de una categoría.</p>
                    <p style="text-align: justify;"><strong>2.a) TIPOS DE COOKIES SEGÚN LA ENTIDAD QUE LAS
                            GESTIONE</strong></p>
                    <p style="text-align: justify;">Según quien sea la entidad que gestione el equipo o dominio desde
                        donde se envían las cookies y trate los datos que se obtengan, podemos distinguir:</p>
                    <ul style="text-align: justify;">
                        <li>Cookies propias: Son aquéllas que se envían al equipo terminal del usuario Desde un equipo o
                            dominio gestionado por el propio editor y desde el que se presta el servicio solicitado por
                            el usuario.
                        </li>
                        <li>Cookies de tercero: Son aquéllas que se envían al equipo terminal del usuario desde un
                            equipo o dominio que no es gestionado por el editor, sino por otra entidad que trata los
                            datos obtenidos través de las cookies.
                        </li>
                    </ul>
                    <p style="text-align: justify;">En el caso de que las cookies sean instaladas desde un equipo o
                        dominio gestionado por el propio editor pero la información que se recoja mediante éstas sea
                        gestionada por un tercero, no pueden ser consideradas como cookies propias.</p>
                    <p style="text-align: justify;"><strong>2.b) TIPOS DE COOKIES SEGÚN EL PLAZO DE TIEMPO QUE
                            PERMANECEN ACTIVADAS</strong></p>
                    <p style="text-align: justify;">Según el plazo de tiempo que permanecen activadas en el equipo
                        terminal podemos distinguir:</p>
                    <ul style="text-align: justify;">
                        <li>Cookies de sesión: Son un tipo de cookies diseñadas para recabar y almacenar datos mientras
                            el usuario accede a una página web. Se suelen emplear para almacenar información que solo
                            interesa conservar para la prestación del servicio solicitado por el usuario en una sola
                            ocasión (p.ej. una lista de productos adquiridos).
                        </li>
                        <li>Cookies persistentes: Son un tipo de cookies en el que los datos siguen almacenados en el
                            terminal y pueden ser accedidos y tratados durante un periodo definido por el responsable de
                            la cookie, y que puede ir de unos minutos a varios años.
                        </li>
                    </ul>
                    <p style="text-align: justify;"><strong>2.c) TIPOS DE COOKIES SEGÚN SU FINALIDAD</strong></p>
                    <p style="text-align: justify;">Según la finalidad para la que se traten los datos obtenidos a
                        través de las cookies, podemos distinguir entre:</p>
                    <ul style="text-align: justify;">
                        <li>Cookies técnicas: Son aquéllas que permiten al usuario la navegación a través de una página
                            web, plataforma o aplicación y la utilización de las diferentes opciones o servicios que en
                            ella existan como, por ejemplo, controlar el tráfico y la comunicación de datos, identificar
                            la sesión, acceder a partes de acceso restringido, recordar los elementos que integran un
                            pedido, realizar el proceso de compra de un pedido, realizar la solicitud de inscripción o
                            participación en un evento, utilizar elementos de seguridad durante la navegación, almacenar
                            contenidos para la difusión de videos o sonido o compartir contenidos a través de redes
                            sociales.
                        </li>
                        <li>Cookies de personalización: Son aquéllas que permiten al usuario acceder al servicio con
                            algunas características de carácter general predefinidas en función de una serie de
                            criterios en el terminal del usuario como por ejemplo serian el idioma, el tipo de navegador
                            a través del cual accede al servicio, la configuración regional desde donde accede al
                            servicio, etc.
                        </li>
                        <li>Cookies de análisis: Son aquéllas que permiten al responsable de las mismas, el seguimiento
                            y análisis del comportamiento de los usuarios de los sitios web a los que están vinculadas.
                            La información recogida mediante este tipo de cookies se utiliza en la medición de la
                            actividad de los sitios web, aplicación o plataforma y para la elaboración de perfiles de
                            navegación de los usuarios de dichos sitios, aplicaciones y plataformas, con el fin de
                            introducir mejoras en función del análisis de los datos de uso que hacen los usuarios del
                            servicio.
                        </li>
                        <li>Cookies publicitarias: Son aquéllas que permiten la gestión, de la forma más eficaz posible,
                            de los espacios publicitarios que, en su caso, el editor haya incluido en una página web,
                            aplicación o plataforma desde la que presta el servicio solicitado en base a criterios como
                            el contenido editado o la frecuencia en la que se muestran los anuncios.
                        </li>
                        <li>Cookies de publicidad comportamental: Son aquéllas que permiten la gestión, de la forma más
                            eficaz posible, de los espacios publicitarios que, en su caso, el editor haya incluido en
                            una página web, aplicación o plataforma desde la que presta el servicio solicitado. Estas
                            cookies almacenan información del comportamiento de los usuarios obtenida a través de la
                            observación continuada de sus hábitos de navegación, lo que permite desarrollar un perfil
                            específico para mostrar publicidad en función del mismo.
                        </li>
                    </ul>
